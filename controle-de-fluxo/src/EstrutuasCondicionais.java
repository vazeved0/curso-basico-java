public class EstrutuasCondicionais {

    /*
     * ESTRUTURAS CONDICIONAIS
     * Possibilidata a escolha de um grupo de ações e comportamentos a serem
     * executadas quando determinadas condições são ou não atendendidas.
     * Estrutura condicional pode ser simples ou composta.
     * 
     * CONDICIONAIS SIMPLES
     * Ocorrem quando a validação da condição de execução de fluxo for positiva,
     * Exemplo:
     * Para sacar o valor em um caixa eletronico o sistema precisa validade "SE"
     * existe ou não aquele valor na conta,
     * "SE" tiver o valor a maquina saca e atualiza o saldo e seguira o fluxo que
     * seria informa saldo. Para ver como isso funcionaria em codigo confira a
     * classe CaixaEletronico.java
     * 
     * 
     * CONDICIONAIS COMPOSTA
     * Ocorre quando o programa precisa seguir mais de uma jornada de execução
     * condicionado a uma regra de negocio.
     * Exemplo: A media de um aluno sera testada em uma condiconal composta, "SE" a
     * media for maior igual a 7 o systema imprime aprovado, SE NÂO o sistema
     * imprime reprovado. Para ver o codigo dessa condicional composta confira a
     * classe ResultadoEscolar.java
     * 
     * 
     * CONDICIONAIS ENCADEADAS
     * Quando em um controle de fluxo existe mais do que IF e ELSE chamamos essas
     * condicionais de encadeadas.
     * Exemplo: A media de um aluno sera testada em uma condiconal composta, "SE" a
     * media for maior igual a 7 o systema imprime aprovado, SE NÂO o sistema
     * imprime reprovado mais não para por ai, existe mais uma condição que será
     * "SE" media maior igual a 5 e menor que 7 o programa vai imprimir Recuperação.
     * Confira a classe ResultadoEscolar2.java para ver o codigo.
     * 
     */

}
